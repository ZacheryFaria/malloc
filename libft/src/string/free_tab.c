#include <stdlib.h>

void	free_tab(char **tab)
{
	int i;

	i = 0;
	if (tab)
	{
		while (tab[i])
		{
			free(tab[i]);
			tab[i] = 0;
			i++;
		}
		free(tab);
	}
}
