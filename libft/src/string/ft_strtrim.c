#include "libft.h"
#include <stdlib.h>

char	*ft_strtrim(char const *s)
{
	char const *fin;

	if (s == NULL)
		return (NULL);
	while (*s == ' ' || *s == '\t' || *s == '\n')
		s++;
	if (*s == '\0')
		return (ft_strnew(0));
	fin = s + ft_strlen(s) - 1;
	while (*fin == ' ' || *fin == '\t' || *fin == '\n')
		fin--;
	return (ft_strsub(s, 0, fin - s + 1));
}
